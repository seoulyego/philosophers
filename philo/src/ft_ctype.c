int	ft_isspace(int c)
{
	return ((9 <= c && c <= 13) || c == 32);
}

int	ft_issign(int c)
{
	return (c == '+' || c == '-');
}

int	ft_isdigit(int c)
{
	return ('0' <= c && c <= '9');
}
